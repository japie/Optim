package org.ftafrica.co.optime.Helper.Succesion;

/**
 *
 * @author Training 8
 */
public class SuccessorMainData {
    private String EmployeeId;
    private double MatchPercentage;
    private String NameAndQualification;
    private String JobTitleAndExpirience;
   
    public SuccessorMainData() {
    }

    public String getEmployeeId() {
        return EmployeeId;
    }

    public void setEmployeeId(String EmployeeId) {
        this.EmployeeId = EmployeeId;
    }

    public double getMatchPercentage() {
        return MatchPercentage;
    }

    public void setMatchPercentage(double MatchPercentage) {
        this.MatchPercentage = MatchPercentage;
    }

    public String getNameAndQualification() {
        return NameAndQualification;
    }

    public void setNameAndQualification(String NameAndQualification) {
        this.NameAndQualification = NameAndQualification;
    }

    public String getJobTitleAndExpirience() {
        return JobTitleAndExpirience;
    }

    public void setJobTitleAndExpirience(String JobTitleAndExpirience) {
        this.JobTitleAndExpirience = JobTitleAndExpirience;
    }
    
    
    
}
