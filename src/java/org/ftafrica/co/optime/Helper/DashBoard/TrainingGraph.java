package org.ftafrica.co.optime.Helper.DashBoard;

/**
 *
 * @author Training 8
 */
public class TrainingGraph {
    int OnTraining;
    int Trained;
    int NotTrained;

    public TrainingGraph() {
    }

    public int getOnTraining() {
        return OnTraining;
    }

    public void setOnTraining(int OnTraining) {
        this.OnTraining = OnTraining;
    }

    public int getTrained() {
        return Trained;
    }

    public void setTrained(int Trained) {
        this.Trained = Trained;
    }

    public int getNotTrained() {
        return NotTrained;
    }

    public void setNotTrained(int NotTrained) {
        this.NotTrained = NotTrained;
    }
    
    
    
}
