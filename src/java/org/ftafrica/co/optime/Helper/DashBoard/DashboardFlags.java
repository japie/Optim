package org.ftafrica.co.optime.Helper.DashBoard;

/**
 *
 * @author Training 8
 */
public class DashboardFlags {
    
    int BookOfWork;
    int OpenRoles;
    int LeavingSoon;
    int OccupiedRoles;
    int Capacity;

    public DashboardFlags() {
    }

    public int getBookOfWork() {
        return BookOfWork;
    }

    public void setBookOfWork(int BookOfWork) {
        this.BookOfWork = BookOfWork;
    }

    public int getOpenRoles() {
        return OpenRoles;
    }

    public void setOpenRoles(int OpenRoles) {
        this.OpenRoles = OpenRoles;
    }

    public int getLeavingSoon() {
        return LeavingSoon;
    }

    public void setLeavingSoon(int LeavingSoon) {
        this.LeavingSoon = LeavingSoon;
    }

    public int getOccupiedRoles() {
        return OccupiedRoles;
    }

    public void setOccupiedRoles(int OccupiedRoles) {
        this.OccupiedRoles = OccupiedRoles;
    }

    public int getCapacity() {
        return Capacity;
    }

    public void setCapacity(int Capacity) {
        this.Capacity = Capacity;
    }
    
    
    
    
}
